function add (a,b) {
    return a+b;
}
function sub (a,b) {
    return a-b;
}
function mul (a,b) {
    return a*b;
}
function div (a,b) {
    return a/b;
}
function rem (a,b) {
    return a%b;
}
function expo (a,b) {
    return a**b;
}
console.log(add(5,6));
console.log(sub(4,8));
console.log(mul(6,7));
console.log(div(10,5));
console.log(rem(8,4));
